function panier() {
    var element = document.getElementById("misenpanier");
    if (element.style.display === "block") {
        element.style.display = "none";
    } else {
        element.style.display = "block";
    }
}

function popsupp() {
    var element = document.getElementById("misenpanier");
    if (element.style.display === "block") {
        element.style.display = "none";
    } else {
        element.style.display = "block";
    }
}